const getScore = (values: RockPaperScissors[], isScore1: boolean): number => {
  // when isScore1 is true, we have the logic expected for Part One, otherwise the logic is for Part Two

  switch (values.join(", ")) {
    // Opponent played Rock
    case `${RockPaperScissors.Rock}, ${RockPaperScissors.Rock}`:
      return isScore1
        ? Outcome.Draw + RockPaperScissors.Rock
        : Outcome.Lost + RockPaperScissors.Scissors;
    case `${RockPaperScissors.Rock}, ${RockPaperScissors.Paper}`:
      return isScore1
        ? Outcome.Win + RockPaperScissors.Paper
        : Outcome.Draw + RockPaperScissors.Rock;
    case `${RockPaperScissors.Rock}, ${RockPaperScissors.Scissors}`:
      return isScore1
        ? Outcome.Lost + RockPaperScissors.Scissors
        : Outcome.Win + RockPaperScissors.Paper;

    // Opponent played Paper
    case `${RockPaperScissors.Paper}, ${RockPaperScissors.Rock}`:
      return Outcome.Lost + RockPaperScissors.Rock;
    case `${RockPaperScissors.Paper}, ${RockPaperScissors.Paper}`:
      return Outcome.Draw + RockPaperScissors.Paper;
    case `${RockPaperScissors.Paper}, ${RockPaperScissors.Scissors}`:
      return Outcome.Win + RockPaperScissors.Scissors;

    // Opponent played Scissors
    case `${RockPaperScissors.Scissors}, ${RockPaperScissors.Rock}`:
      return isScore1
        ? Outcome.Win + RockPaperScissors.Rock
        : Outcome.Lost + RockPaperScissors.Paper;
    case `${RockPaperScissors.Scissors}, ${RockPaperScissors.Paper}`:
      return isScore1
        ? Outcome.Lost + RockPaperScissors.Paper
        : Outcome.Draw + RockPaperScissors.Scissors;
    case `${RockPaperScissors.Scissors}, ${RockPaperScissors.Scissors}`:
      return isScore1
        ? Outcome.Draw + RockPaperScissors.Scissors
        : Outcome.Win + RockPaperScissors.Rock;
    default:
      return 0;
  }
};

const getTotalScore = (
  arrays: RockPaperScissors[][],
  isResult1: boolean
): number => {
  const arrayScores = arrays.map((array) => {
    return getScore(array, isResult1);
  });

  return arrayScores.reduce((a, b) => a + b, 0);
};

